    private void btn_submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_submitActionPerformed
        ControllerTransaksi ct = new ControllerTransaksi();
        int jumlah = Integer.parseInt(txtJumlah.getText());
        double total = jumlah * ct.pembelian(txtId.getText());
      
        txtTotalHarga.setText(Double.toString(total));
        
        boolean hasil = ct.insertTransaksi(txtId.getText(), txtIdPembeli.getText(), Integer.parseInt(txtJumlah.getText()), total);
        if (hasil) {
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan");
            loadData();
        }else{
            JOptionPane.showMessageDialog(null, "Data gagal ditambahkan", "Pesan", 
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_submitActionPerformed

